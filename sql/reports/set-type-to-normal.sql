SELECT
  CLASS.CLASS_ID
FROM CLASS
INNER JOIN CLASS_TYPE ON CLASS_TYPE.CLASS_TYPE_ID = CLASS.CLASS_TYPE_ID
WHERE CLASS.ACADEMIC_YEAR_ID = 45 AND CLASS.CLASS_TYPE_ID = 1028 AND CLASS.CLASS LIKE '11%'
)

