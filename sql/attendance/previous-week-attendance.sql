WITH RAW_HR1 AS
(
  SELECT
		STUDENT.STUDENT_ID,
		CONTACT.SURNAME,
		CONTACT.FIRSTNAME,
		CONTACT.GENDER_ID,
		FORM_RUN.FORM_RUN,
		DATE_ON,
		LATE_TIME_IN,
		ATTEND_STATUS_ID,
		CASE ABSENT_STATUS
		  WHEN 0 THEN 'Not absent/Unexplained'
		  WHEN 1 THEN 'Explained - Unverified'
		  WHEN 2 THEN 'Explained - Verified'
		  WHEN 3 THEN 'Explained - Verified (Sick)'
		  WHEN 4 THEN 'Explained - Verified (Suspended)'
		  WHEN 5 THEN 'Explained - Verified (Approved By Principal)'
		  WHEN 6 THEN 'Explained - Verified (School Business)'
		  WHEN 7 THEN 'Explained - Verified (Flexible Timetable)'
		  WHEN 8 THEN 'Explained - Verified (External Education)'
		  WHEN 9 THEN 'Explained - Verified (Exempt)'
		END AS "ABSENT_STATUS_HR1",
		PERIOD.PERIOD AS "HR1"
	
	FROM VIEW_ATTENDANCE VATTEND1
	
	INNER JOIN STUDENT ON STUDENT.STUDENT_ID = VATTEND1.STUDENT_ID
	INNER JOIN CONTACT ON CONTACT.CONTACT_ID = STUDENT.CONTACT_ID
	
	INNER JOIN TABLE(EDUMATE.GET_ENROLED_STUDENTS_FORM_RUN(current_date)) GESF ON VATTEND1.STUDENT_ID = GESF.STUDENT_ID
	INNER JOIN FORM_RUN ON FORM_RUN.FORM_RUN_ID = GESF.FORM_RUN_ID

	INNER JOIN PERIOD ON PERIOD.PERIOD_ID = VATTEND1.PERIOD_ID

	WHERE
		DATE_ON >= (CURRENT_DATE - 7 DAYS)
			AND
		ATTEND_STATUS_ID = 3
			AND
		ABSENT_STATUS IN (0,1)
			AND
		PERIOD = 'Home Room 1'
),

RAW_HR2 AS
(
  SELECT
  		STUDENT.STUDENT_ID,
  		PERIOD.PERIOD AS "HR2",
  		CASE ABSENT_STATUS
		  WHEN 0 THEN 'Not absent/Unexplained'
		  WHEN 1 THEN 'Explained - Unverified'
		  WHEN 2 THEN 'Explained - Verified'
		  WHEN 3 THEN 'Explained - Verified (Sick)'
		  WHEN 4 THEN 'Explained - Verified (Suspended)'
		  WHEN 5 THEN 'Explained - Verified (Approved By Principal)'
		  WHEN 6 THEN 'Explained - Verified (School Business)'
		  WHEN 7 THEN 'Explained - Verified (Flexible Timetable)'
		  WHEN 8 THEN 'Explained - Verified (External Education)'
		  WHEN 9 THEN 'Explained - Verified (Exempt)'
		END AS "ABSENT_STATUS_HR2"
	
	FROM VIEW_ATTENDANCE VATTEND2
	
	INNER JOIN STUDENT ON STUDENT.STUDENT_ID = VATTEND2.STUDENT_ID
	INNER JOIN PERIOD ON PERIOD.PERIOD_ID = VATTEND2.PERIOD_ID

	WHERE
		DATE_ON >= (CURRENT_DATE - 7 DAYS)
			AND
		ATTEND_STATUS_ID = 3
			AND
		ABSENT_STATUS IN (0,1)
			AND
		PERIOD.PERIOD = 'Home Room 2'
),

RAW_DATA AS
(
	SELECT DISTINCT
			SURNAME,
			FIRSTNAME,
			GENDER_ID,
			FORM_RUN,
			DATE_ON,
			LATE_TIME_IN,
			ATTEND_STATUS_ID,
			ABSENT_STATUS_HR1,
			HR1,
			RAW_HR2.HR2,
			RAW_HR2.ABSENT_STATUS_HR2
	
	FROM RAW_HR1
	
	INNER JOIN RAW_HR2 ON RAW_HR2.STUDENT_ID = RAW_HR1.STUDENT_ID
	
	ORDER BY DATE_ON DESC, SURNAME ASC
)

