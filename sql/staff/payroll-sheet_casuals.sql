SELECT
  CONTACT.SURNAME,
  CONTACT.FIRSTNAME,
  TO_CHAR((AC.FROM_DATE), 'DD Month, YYYY') AS "DATES",
  CASE WHEN AC.TIME_WORKED = 1 THEN 'Half Day' ELSE 'Full day' END AS "ALLOCATION"

FROM AVAILABLE_CASUAL AC

INNER JOIN STAFF ON STAFF.STAFF_ID = AC.STAFF_ID
INNER JOIN CONTACT ON CONTACT.CONTACT_ID = STAFF.CONTACT_ID
INNER JOIN STAFF_EMPLOYMENT SE ON SE.STAFF_ID = AC.STAFF_ID AND SE.EMPLOYMENT_TYPE_ID IN (3,4)

WHERE
  DATE(AC.FROM_DATE) >= DATE('[[mainquery.REPORT_START]]')
  AND
  DATE(AC.TO_DATE) <= DATE('[[mainquery.REPORT_END]]')

ORDER BY CONTACT.SURNAME, AC.FROM_DATE